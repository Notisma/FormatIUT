    L'ensemble des éléments composant le site, notamment les textes, les images, les vidéos, les logos, les marques, les bases de données, est protégé par la propriété intellectuelle.

    L'utilisateur s'engage à ne pas reproduire, copier, modifier, altérer, transmettre, diffuser, exploiter, commercialiser, ou distribuer, de quelque manière que ce soit, les éléments composant le site, sans l'autorisation expresse et préalable de l'éditeur.

