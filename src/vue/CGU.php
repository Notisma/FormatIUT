    L'utilisateur s'engage à utiliser le site conformément aux lois et règlements en vigueur, ainsi qu'aux présentes CGU.

    L'utilisateur s'engage notamment à ne pas :

    diffuser des contenus illicites ou contraires aux bonnes mœurs ;
    diffuser des contenus qui portent atteinte aux droits des tiers ;
    diffuser des contenus susceptibles de nuire à l'image du site ou de ses utilisateurs ;
    envoyer des messages à caractère publicitaire ou promotionnel non sollicités.
    Article 5 - Responsabilité

    Le site est accessible 24h/24 et 7j/7, sauf en cas de force majeure ou de maintenance.

    L'éditeur ne peut garantir la continuité et la qualité du service fourni par le site.

    L'éditeur ne peut être tenu responsable des dommages directs ou indirects, matériels ou immatériels, causés à l'utilisateur, ou à des tiers, par l'utilisation du site.

