    Le site est édité par l'équipe de Format'IUY.
    L'hébergement du site est assuré par l'IUT de Montpellier.

    Article 3 - Accès au site

    L'accès au site est gratuit et libre.

    Toutefois, l'utilisateur doit s'identifier pour pouvoir utiliser certaines fonctionnalités du site, telles que la création d'un compte, la publication d'une offre de stage ou d'alternance, ou la candidature à une offre de stage ou d'alternance.

